package while_loop;

import java.util.Scanner;

public class While3 {
	public static void main(String[] args) {
		int n;
		System.out.println("Enter Number to check condition");
		Scanner ref = new Scanner(System.in);
		n = ref.nextInt();
		while (n >=10 && n<=10) {
			System.out.println("Hello");
		}
	}

}
