package for_loop;

public class For {
	public static void main(String[] args) {
		for(int i=1; i<=10; i++) {
			System.out.print((i)+ "\n");
		}
		
		System.out.println("Reverse");
		for(int x=10; x>=1; x--) {
			System.out.print(x+ " ");
		}
		
		for(int a=10; a<=20; a++) {
			System.out.println(a);
		}
		
		for(int b=20; b>=10; b--) {
			System.out.println(b);
		}
	}

}
